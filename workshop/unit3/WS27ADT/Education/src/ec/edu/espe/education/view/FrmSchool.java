    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        School school;
        String name;
        String location;
        int zone;
        int circuit;
        int numberOfStudents;
        SchoolController schoolController = new SchoolController();

        name = txtName.getText();
        location = cmbLocation.getSelectedItem().toString();
        zone = Integer.parseInt(txtZone.getSelectedText());
        circuit = Integer.parseInt(txtCircuit.getSelectedText());
        numberOfStudents = Integer.parseInt(txtNumberOfStudents.getSelectedText());

        school = new School(name, location, zone, circuit, numberOfStudents);

        schoolController.save(school);

        JOptionPane.showMessageDialog(rootPane, school.getName());

    }//GEN-LAST:event_btnSaveActionPerformed

